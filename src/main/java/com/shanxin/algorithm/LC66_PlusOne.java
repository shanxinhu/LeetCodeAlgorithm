package com.shanxin.algorithm;

public class LC66_PlusOne {

    /*
   https://leetcode.cn/problems/plus-one/
    66. 加一
        简单
        相关标签
        premium lock icon
        相关企业
        给定一个表示 大整数 的整数数组 digits，其中 digits[i] 是整数的第 i 位数字。这些数字按从左到右，从最高位到最低位排列。这个大整数不包含任何前导 0。

        将大整数加 1，并返回结果的数字数组。



        示例 1：

        输入：digits = [1,2,3]
        输出：[1,2,4]
        解释：输入数组表示数字 123。
        加 1 后得到 123 + 1 = 124。
        因此，结果应该是 [1,2,4]。
        示例 2：

        输入：digits = [4,3,2,1]
        输出：[4,3,2,2]
        解释：输入数组表示数字 4321。
        加 1 后得到 4321 + 1 = 4322。
        因此，结果应该是 [4,3,2,2]。
        示例 3：

        输入：digits = [9]
        输出：[1,0]
        解释：输入数组表示数字 9。
        加 1 得到了 9 + 1 = 10。
        因此，结果应该是 [1,0]。


        提示：

        1 <= digits.length <= 100
        0 <= digits[i] <= 9
        digits 不包含任何前导 0。
     */

    /**
     * 从最低位开始加一，处理进位
     * 遇到非9的位直接加一并返回；遇到9则置0继续向高位进位。
     * 若所有位都为9（如 999 → 1000），需创建长度+1的新数组。
     *
     * 时间复杂度：O(n)，其中 n 为数组长度，最坏情况遍历所有位（全为9时）。
     * 空间复杂度：O(1)，除全9情况外无额外空间；全9时需要 O(n) 创建新数组。
     */
    public int[] plusOne(int[] digits) {
        int n = digits.length;
        // 从最低位向最高位遍历
        for (int i = n - 1; i >= 0; i--) {
            if (digits[i] == 9) {
                // 当前位为9，加一后变为0，向高位进位
                digits[i] = 0;
            } else {
                // 当前位非9，加一后无进位，直接返回
                digits[i]++;
                return digits;
            }
        }
        // 所有位都为9，如 999 → 1000
        int[] ans = new int[n + 1];
        ans[0] = 1;
        return ans;
    }

}
